// 从第 n 个系统调用参数中获取文件描述符，并返回描述符和对应的 struct file。
// 参数：
// - n: 第 n 个参数
// - pfd: 用于存储文件描述符的指针（可选）
// - pf: 用于存储 struct file 指针的指针（可选）
// 返回值：
// - 成功返回 0，失败返回 -1
static int
argfd(int n, int *pfd, struct file **pf)
{
  int fd;
  struct file *f;

  argint(n, &fd);
  if(fd < 0 || fd >= NOFILE || (f=myproc()->ofile[fd]) == 0)
    return -1;
  if(pfd)
    *pfd = fd;
  if(pf)
    *pf = f;
  return 0;
}

// 为给定文件分配一个文件描述符。
// 参数：
// - f: 指向 struct file 的指针
// 返回值：
// - 成功返回文件描述符，失败返回 -1
static int
fdalloc(struct file *f)
{
  int fd;
  struct proc *p = myproc();

  for(fd = 0; fd < NOFILE; fd++){
    if(p->ofile[fd] == 0){
      p->ofile[fd] = f;
      return fd;
    }
  }
  return -1;
}

// 复制文件描述符。
// 返回值：
// - 成功返回新的文件描述符，失败返回 -1
uint64
sys_dup(void)
{
  struct file *f;
  int fd;

  if(argfd(0, 0, &f) < 0)
    return -1;
  if((fd=fdalloc(f)) < 0)
    return -1;
  filedup(f);
  return fd;
}

// 读取文件内容。
// 参数：
// - 无显式参数，通过系统调用接口传递
// 返回值：
// - 成功返回读取的字节数，失败返回 -1
uint64
sys_read(void)
{
  struct file *f;
  int n;
  uint64 p;

  argaddr(1, &p);
  argint(2, &n);
  if(argfd(0, 0, &f) < 0)
    return -1;
  return fileread(f, p, n);
}

